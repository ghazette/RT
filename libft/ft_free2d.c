#include <stdlib.h>

void	ft_free2d(char ***str)
{
	int i;

	i = 0;
	while ((*str)[i] != 0)
	{
		free((*str)[i]);
		i++;
	}
	free((*str)[i]);
	free(*str);
	*str = NULL;
}
