#include "includes/libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t l;
	size_t i;

	i = 0;
	l = ft_strlen(dest);
	while (i < n && src[i] != '\0')
	{
		dest[l + i] = src[i];
		i++;
	}
	dest[l + i] = '\0';
	return (dest);
}
